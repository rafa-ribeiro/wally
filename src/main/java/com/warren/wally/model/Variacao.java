package com.warren.wally.model;

public class Variacao {

	private double mensalPorcentagem;
	private double mensalAbsoluto;
	private double anualPorcentagem;
	private double anualAbsoluto;
	
	public double getMensalPorcentagem() {
		return mensalPorcentagem;
	}
	public void setMensalPorcentagem(double mensalPorcentagem) {
		this.mensalPorcentagem = mensalPorcentagem;
	}
	public double getMensalAbsoluto() {
		return mensalAbsoluto;
	}
	public void setMensalAbsoluto(double mensalAbsoluto) {
		this.mensalAbsoluto = mensalAbsoluto;
	}
	public double getAnualPorcentagem() {
		return anualPorcentagem;
	}
	public void setAnualPorcentagem(double anualPorcentagem) {
		this.anualPorcentagem = anualPorcentagem;
	}
	public double getAnualAbsoluto() {
		return anualAbsoluto;
	}
	public void setAnualAbsoluto(double anualAbsoluto) {
		this.anualAbsoluto = anualAbsoluto;
	}
	
	
}
